    public function store(Request $request)
    {
        $user_id = $request->user_id;
        $branch_id = (int) $request->branch_id;

        $user = User::find($user_id);
        $user->user_branch = intval($branch_id);
        $user->save();

        return response()->json(['status' => $user_id]);

